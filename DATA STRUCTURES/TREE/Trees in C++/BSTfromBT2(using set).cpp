//store the inorder traversal in a set so it is already sorted(as sets are implemented with self balancing BST)
//now copy from the set

//only difference from the previous method is that we don't have to sort exclusively
//and the sorting itself is done in O(log n)

//utility function to store inorder
//stores *nodes* in inorder way
void storeInorderUtil(TreeNode* root,set<int>& s)
{

    if(root)
    {
        storeInorderUtil(root->left,s);

        s.insert(root->data);

        storeInorderUtil(root->right,s);
    }
}


//now copy from set

void copyFromSet(TreeNode* root,set<int>& s)
{
    if(root)
    {
        copyFromSet(root->left,s);

        if(!s.empty())
        {

            root->data=*s.begin();

            s.erase(s.begin());
        }

        copyFromSet(root->right,s);
    }
}

BST BSTfromBT2(Tree t)
{
    set<int> s;

    storeInorderUtil(t.root,s);

    //converts the BT itself to a BST
    copyFromSet(t.root,s);

    BST bst;

    bst.root=t.root;

    return bst;
}

