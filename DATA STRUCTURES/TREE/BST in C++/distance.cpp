//!function to find lca

//assuming both n1 and n2 exists

//LCA is the first node which has n1 and n2 in different subtrees

//if one in any subtree of the other then the other node is the LCA





//!function to find distance of a node from root(level)
//assuming they both exist
int findLevel(TreeNode* root,int n)
{

    if(root->data==n)
        return 0;

    else if(root->data>n)
        return 1+findLevel(root->left,n);

    return 1+findLevel(root->right,n);
}


//here we find LCA and return summation of the distances of the nodes from LCA
int distance(TreeNode* root,int n1,int n2)
{
    if(!root)
        return 0;

    if(root->data>n1 && root->data>n2)
        return distance(root->left,n1,n2);

    else if(root->data<n1 && root->data<n2)
        return distance(root->right,n1,n2);

     return findLevel(root,n1) + findLevel(root,n2);
}

